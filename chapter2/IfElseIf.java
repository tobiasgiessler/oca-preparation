public class IfElseIf {
  public static void main(String[] args) {
    int x = 5;

    if (x < 4) {
      System.out.println("if 1");
    } else if (x < 9) {
      System.out.println("if 2");
    } else if (x < 8) {
      System.out.println("if 3");
    } else {
      System.out.println("else");
    }
  }
}
